interface Data {
  name: string;
  code: string;
  population: number;
  size: number;
  density: number;
  actions?: any; // Agrega el campo actions para evitar el error
}

function createData(
  name: string,
  code: string,
  population: number,
  size: number,
): Data {
  const density = population / size;
  return { name, code, population, size, density };
}

